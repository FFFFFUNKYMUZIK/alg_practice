class Solution {


    typedef long long ll;

public:
    int uniquePathsWithObstacles(vector<vector<int>>& obstacleGrid) {

        int n = obstacleGrid.size();
        int m = obstacleGrid[0].size();

        vector<ll> dp(m, 0);
        dp[0] = 1;
        
        int row = 0;
        while (row++ < n) {
            dp[0] = (1 - obstacleGrid[row-1][0])*dp[0];
            for (int i = 1; i < m; i++) {
                dp[i] = (dp[i] + dp[i - 1]) * (1-obstacleGrid[row-1][i]);
            }
        }

        return dp[m-1];

    }
};

