using namespace std;

class Solution {
public:
    bool search(vector<int>& nums, int target) {
        
        int n = nums.size();
        if (n == 0) return false;

        int s = 0, e = n-1;
        int mid, pivot = 0;

        while (s <= e) {
            mid = (s + e) / 2;
            if (nums[mid]>nums[e]){
                s = mid + 1;
            }
            else if (nums[mid] < nums[e]) {
                e = mid;
            }
            else {
                if (nums[mid] > nums[s]) {
                    pivot = s;
                    break;
                }
                else if (nums[mid] < nums[s]) {
                    s++;
                }
                else {
                    while (s < e && nums[e] == nums[e - 1]) e--;
                    while (s < e && nums[s] == nums[s + 1]) s++;
                }

                if (s == e) {
                    pivot = s;
                    break;
                }
            }
        }

        s = 0, e = n - 1;
        int rot_idx;
        while (s <= e) {
            mid = (s + e) / 2;
            rot_idx = (mid + pivot) % n;
            if (nums[rot_idx] < target) {
                s = mid + 1;
            }
            else if (nums[rot_idx] > target) {
                e = mid - 1;
            }
            else {
                return true;
            }
        }

        return false;
    }

};

